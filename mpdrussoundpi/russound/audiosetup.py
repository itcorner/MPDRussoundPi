def acquire_lock():
    """Acquire a lock to prevent multiple instances of the script from running simultaneously."""
    import os
    import sys

    if os.path.exists(LOCKFILE):
        print("Another instance of the script is already running. Exiting.")
        print(f"If you are sure no other instance is running, delete the lock file: {LOCKFILE}")
        sys.exit(1)

    with open(LOCKFILE, 'w') as f:
        f.write(str(os.getpid()))

def release_lock():
    """Release the lock when the script finishes."""
    import os

    if os.path.exists(LOCKFILE):
        # Check if the PID in the lock file matches the current process before removing it
        with open(LOCKFILE, 'r') as f:
            pid = f.read()
            if pid == str(os.getpid()):
                os.remove(LOCKFILE)


def detect_audio_processor() -> dict[str, bool]:
    """Detect active audio processor and return the selected processor."""
    pipewire_active = subprocess.run(['systemctl', '--user', 'is-active', 'pipewire'], 
                                        capture_output=True).returncode == 0
    pulseaudio_active = subprocess.run(['systemctl', '--user', 'is-active', 'pulseaudio'], 
                                        capture_output=True).returncode == 0
    processors : dict[str, bool] = {}
    processors['pipewire'] = pipewire_active
    processors['pulseaudio'] = pulseaudio_active
    logging.debug(f"Detected audio processor status: {processors}")
    return processors

def create_pipewire_config(device_dict: dict[str, AudioDevice]):
    """Create PipeWire configuration file based on the provided device dictionary."""
    print("Creating PipeWire configuration...")
    logging.debug(f"Creating PipeWire config with devices: {list(device_dict.keys())}")
    os.makedirs(os.path.dirname(PIPEWIRE_CONFIG), exist_ok=True)

    with open(PIPEWIRE_CONFIG, 'w') as f:
        f.write("# This file is auto-generated by MPDRussoundPi audiosetup.py\n")
        f.write("# Do not edit this file directly. Instead, edit config.json and run audiosetup.py with --initialize option.\n\n")
        f.write("# PipeWire config for remapped sinks\n\n")
        f.write("context.modules = [\n")
        for device in device_dict.values():
            f.write(device.get_pipewire_sink_config())
        f.write("]\n")

    print(f"Created PipeWire config: {PIPEWIRE_CONFIG}")
    #subprocess.run(['systemctl', '--user', 'restart', 'pipewire'], check=False)

